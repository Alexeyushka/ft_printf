#include "libft.h"

int		ft_strlen_double(char *str)
{
	int i;
	int len;

	i = 0;
	len = 0;
	while (str[i] != '\0')
	{
		if (str[i] == '.')
		{
			while (str[i] != '\0')
			{
				len++;
				i++;
			}
		}
		i++;
	}
	return (len);
}
